"""
Experiment 3: Token Audit of Concrete Research Session Traces

Approach:
1. Find all hypothesis files in the token-efficiency-researching-skill directory.
2. Count words in each file and in its component sections (Statement+Rationale,
   Literature, Experiments, Results).
3. Assess whether session traces with completed Steps B-D exist.
4. For the available data (hypothesis stubs + literature sections), compute
   per-hypothesis word counts and project N=2 vs N=4 savings.
5. Compare against the 37,000-word baseline from problem.md.
"""

import os
import re
import sys

SESSION_DIR = "/home/fence/src/pipemind/claude-agentic/plugins/scientific-method/token-efficiency-researching-skill"

def count_words(text):
    return len(text.split())

def extract_sections(content):
    sections = {}

    # Extract Statement
    m = re.search(r'## Statement\n(.*?)(?=\n##|\Z)', content, re.DOTALL)
    sections['statement'] = m.group(1).strip() if m else ""

    # Extract Rationale
    m = re.search(r'## Rationale\n(.*?)(?=\n##|\Z)', content, re.DOTALL)
    sections['rationale'] = m.group(1).strip() if m else ""

    # Extract Literature
    m = re.search(r'## Literature\n(.*?)(?=\n##|\Z)', content, re.DOTALL)
    sections['literature'] = m.group(1).strip() if m else ""

    # Extract Refined Statement
    m = re.search(r'## Refined Statement\n(.*?)(?=\n##|\Z)', content, re.DOTALL)
    sections['refined_statement'] = m.group(1).strip() if m else ""

    # Extract Experiments
    m = re.search(r'## Experiments\n(.*?)(?=\n## Conclusion|\Z)', content, re.DOTALL)
    sections['experiments'] = m.group(1).strip() if m else ""

    # Extract Results content (all filled results sections)
    results = re.findall(r'#### Results\n(.*?)(?=\n---|\n### |\Z)', content, re.DOTALL)
    filled = [r.strip() for r in results
              if r.strip() and r.strip() != '<!-- To be filled by running-experiments -->']
    sections['results'] = "\n\n".join(filled)

    return sections

print("=" * 70)
print("HYPOTHESIS FILE WORD COUNT AUDIT")
print("=" * 70)

hyp_files = sorted([
    os.path.join(SESSION_DIR, f)
    for f in os.listdir(SESSION_DIR)
    if f.startswith("hypothesis-") and f.endswith(".md")
])

print(f"\nFound {len(hyp_files)} hypothesis files.\n")

total_words_all = 0
per_hyp_data = []

for fpath in hyp_files:
    fname = os.path.basename(fpath)
    with open(fpath) as f:
        content = f.read()

    total_words = count_words(content)
    sections = extract_sections(content)

    w_statement = count_words(sections['statement']) + count_words(sections['rationale'])
    w_lit = count_words(sections['literature']) + count_words(sections['refined_statement'])
    w_experiments = count_words(sections['experiments'])
    w_results = count_words(sections['results'])
    has_lit = bool(sections['literature'])
    has_results = bool(sections['results'])

    per_hyp_data.append({
        'file': fname,
        'total': total_words,
        'statement': w_statement,
        'literature': w_lit,
        'experiments': w_experiments,
        'results': w_results,
        'has_lit': has_lit,
        'has_results': has_results,
    })

    total_words_all += total_words

    print(f"{fname}:")
    print(f"  Total words: {total_words}")
    print(f"  Statement+Rationale: {w_statement}  |  Literature+Refined: {w_lit}  |  Experiments: {w_experiments}  |  Results: {w_results}")
    print(f"  Has literature: {has_lit}  |  Has results: {has_results}")

print(f"\nTotal words across all {len(hyp_files)} hypothesis files: {total_words_all}")

# Check if any files have full Steps B-D completed (literature + experiment results)
completed = [d for d in per_hyp_data if d['has_lit'] and d['has_results']]
has_lit_only = [d for d in per_hyp_data if d['has_lit'] and not d['has_results']]
stub_only = [d for d in per_hyp_data if not d['has_lit'] and not d['has_results']]

print(f"\nCompletion status:")
print(f"  Files with literature AND results (Steps B-E): {len(completed)}")
print(f"  Files with literature only (Step B done): {len(has_lit_only)}")
print(f"  Stub-only (Step A done): {len(stub_only)}")

# Since no files have results, we can still analyze the literature section sizes
# for files that have been through Step B (refine-hypothesis)
if has_lit_only:
    lit_words = [d['literature'] for d in has_lit_only]
    avg_lit = sum(lit_words) / len(lit_words)
    print(f"\n  Literature section words (files with Step B done):")
    for d in has_lit_only:
        print(f"    {d['file']}: {d['literature']} words")
    print(f"  Average literature words per hypothesis: {avg_lit:.0f}")

    # Estimate: if we assume these were generated by 4 literature invocations,
    # each invocation generated avg_lit / 4 words of content
    gen_per_lit_invoke = avg_lit / 4
    print(f"\n  Estimated generated content per lit invocation: {gen_per_lit_invoke:.0f} words")

# Projection: use the available data to estimate N=2 vs N=4 savings
print("\n" + "=" * 70)
print("PROJECTION: N=2 vs N=4 Token Savings")
print("=" * 70)

# From problem.md baseline
total_baseline_N4 = 37000
lit_fraction = 0.52
lit_baseline = total_baseline_N4 * lit_fraction

print(f"\nProblem.md baseline (N=4): {total_baseline_N4} words")
print(f"Literature share: {lit_baseline:.0f} words ({lit_fraction*100:.0f}%)")

# Average words per hypothesis file (stub phase = Step A output)
avg_stub_words = sum(d['total'] for d in per_hyp_data) / len(per_hyp_data)
print(f"\nAverage hypothesis stub (Step A) size: {avg_stub_words:.0f} words")

# The 37,000 word total includes instruction loading from skill files (fixed per invocation)
# plus generated content. We only have the generated content (hypothesis files).
# The total hypothesis file size across 8 hypotheses = {total_words_all} words
# If this were a single 4-hypothesis session, the 4 hypothesis stubs would total ~4x avg

avg_4hyp_stub = avg_stub_words * 4
print(f"Estimated 4-hypothesis stub content: {avg_4hyp_stub:.0f} words")
print(f"Estimated 2-hypothesis stub content: {avg_stub_words * 2:.0f} words")

# The stub content is a small fraction of the 37,000 total (mostly instruction overhead)
stub_fraction = avg_4hyp_stub / total_baseline_N4
print(f"Stub content as fraction of total: {stub_fraction*100:.1f}%")

# Conclusion: the generated content in hypothesis stubs is small (~10-15% of total)
# This means instruction loading dominates, consistent with the hypothesis
print(f"\nConclusion:")
print(f"  Hypothesis stub content ({avg_4hyp_stub:.0f} words) = {stub_fraction*100:.1f}% of total {total_baseline_N4} words")
print(f"  Instruction overhead dominates: ~{100-stub_fraction*100:.1f}% of total is skill instruction loading")
print(f"\nWith literature sections (from Step B refine-hypothesis):")
if has_lit_only:
    avg_lit_per_hyp = sum(d['literature'] for d in has_lit_only) / len(has_lit_only)
    print(f"  Average literature section: {avg_lit_per_hyp:.0f} words per hypothesis")
    lit_4hyp = avg_lit_per_hyp * 4
    print(f"  4-hypothesis literature content: {lit_4hyp:.0f} words")
    gen_fraction = (avg_4hyp_stub + lit_4hyp) / total_baseline_N4
    print(f"  Total generated content fraction: {gen_fraction*100:.1f}%")

print("\n  Note: No completed sessions (Steps B-E all done) were found in this")
print("  research directory. Word count analysis is limited to stub-phase files")
print("  and literature sections (where available). The baseline figures from")
print("  problem.md are the primary anchor for the token model.")
